
public class B10_Operator4 {

	public static void main(String[] args) {
		
		/*
		  	# 대입 연산자
		  	
		  	  - 왼쪽의 변수에 오른쪽의 값을 대입한다

		 */
		
		int num = 10;
		num = num + 20; //(num + 20먼저 계산) 고로 30이 나온다
		num = num + 5; //35
		
		System.out.println(num);
		
		/*
		 	# 복합 대입 연산자
		 		
		 	 - 변수에 결과를 누적시키는 연산	 	
		 */
		
		num = 10;
		System.out.println(num += 15); // num = num + 5
		System.out.println(num -= 3);  // num = num - 3
		System.out.println(num *= 2);  // num = num * 2
		System.out.println(num /= 4);  // num = num / 4
		System.out.println(num %= 3);  // num = num % 3
		
		/*
		 	# 단항 연산자
		 	  
		 	  - 변수에 결과를 1씩 누적시키는 연산자
		 	  - ++, --의 위치에 따라 결과가 달라진다
		 */
		
		num = 10;
		num++; // num = num + 1 //1씩 증가
		System.out.println(num);
		
		num--; // num = num - 1 //1씩 감소
		System.out.println(num);
		
		//++, --를 뒤에 붙이면 해당 줄의 모든 명령을 실행한 후에 값이 변한다
		//++, --를 앞에 붙이면 해당 줄의 명령을 실행하기 전에 값이 변한다
		num = 3;
		System.out.println(num++); // 3이 나온다
		System.out.println(num); // or (++num) 시 값 4가 나온다
		
		System.out.println(num--); // 고로 4 가 나옴 먼저 위에 실행이 되어서?
		System.out.println(num); // 3
		
		
		
		
		
		
	}
	
	
	
	
}
